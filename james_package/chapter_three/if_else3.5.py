number_1 = int(input("Enter first_number: "))
number_2 = int(input("Enter second_number: "))
if number_1 == number_2:
    print("number_1 is equal to number_2")
else:
    print("number_1 is not equal to number_2")
if number_1 < number_2:
    print("number_1 is lesser than number_2")
else:
    print("number_1 is not lesser than number_2")
if number_1 <= number_2:
    print("number_1 is lesser or equal number_2")
else:
    print("number_1 is greater or equal number_2 ")





